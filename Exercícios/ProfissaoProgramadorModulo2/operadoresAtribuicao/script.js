let numero1 = 6;
const numero2 = 2;

//operadores de atribuição

// =
// +=
// x += y --> x = x + y
// -=
// x -= y --> x = x - y
// *=
// x *= y --> x = x * y

const adicao = numero1 += numero2; // numero1 = numero1 + numero2
const subtracao = numero1 -= numero2; // numero1 = numero1 - numero2
const multiplicacao = numero1 *= numero2; // numero1 = numero1 * numero2

console.log(adicao);
console.log(subtracao);
console.log(multiplicacao);