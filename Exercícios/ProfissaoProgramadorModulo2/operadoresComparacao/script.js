const numero1 = 6;
const numero2 = 2;
const numero3 = '6';

// operadores de comparação

console.log(numero1 > numero2); // true
console.log(numero1 < numero2); // false
console.log(numero1 >= numero2); // true
console.log(numero1 <= numero2); // false
console.log(numero1 == numero2); // false
console.log(numero1 != numero2); // true
console.log(numero1 === numero3); // false - verifica se são identicos
console.log(numero1 !== numero3); // true - verifica se são identicos
console.log(numero1 == numero3); //true - só verifica se são iguais mesmo de tipos diferentes
console.log(numero1 != numero3); //false - só verifica se são iguais mesmo de tipos diferentes